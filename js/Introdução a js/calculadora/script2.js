// TIPOS DE FUNÇÃO 

// DECLARATIVA
function funDeclarativa () {
    console.log('Msg de uma função Declarativa')
}

funDeclarativa();

// Expressõe de funções
var funcao = function () {
    console.log('Msg de uma função de expressao')
}
funcao();

// Arrow Function

var aFuncao = () => {
    console.log('Msg de uma função de Arrow')
}

aFuncao();